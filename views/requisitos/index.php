    tendran mayor <b>probabilidad</b> de selección</p>
    <p align="justify"> Seguidamente se pasa a la fase de observación <b>deportiva</b>, donde los aspirantes siguen un programa deportivo
    preliminar y los entrenadores evaluan cualitativa y cuantitativamente las capacidades condicionales mas importantes
    de cada deporte</p>
    <p align="justify">Una vez terminada esta fase, los aspirantes tienen como requisito realizar <b>pruebas medicas y psicologicas</b> complementarias.</p>
